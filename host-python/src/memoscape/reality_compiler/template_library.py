register(BehaviorTemplate(
    name="react_timer",
    lua_template='''\
-- react_timer.lua  (generated by Reality Compiler)
local MIN_MS   = ${min_delay_ms}
local MAX_MS   = ${max_delay_ms}
local waiting  = false
local flash_t  = 0

math.randomseed(os.clock())

local function next_round()
    frame.display.text("Get ready...", 10, 80)
    frame.display.show()
    local delay = MIN_MS + math.random(MAX_MS - MIN_MS)
    frame.sleep(delay / 1000.0)
    flash_t   = os.clock()
    waiting   = true
    frame.display.text("GO!", 10, 80)
    frame.display.show()
end

frame.button.${trigger}(function()
    if waiting then
        local reaction_ms = math.floor((os.clock() - flash_t) * 1000)
        waiting = false
        frame.display.text(tostring(reaction_ms) .. " ms", 10, 80)
        frame.display.show()
        frame.sleep(2.0)
        next_round()
    else
        next_round()
    end
end)

frame.display.text("Tap to start", 10, 80)
frame.display.show()
while true do frame.sleep(0.1) end
''',
    host_template=_HOST_TEMPLATE,
    test_spec={"inject": "single_click", "wait_ms": 300, "assert_bright": True},
))
