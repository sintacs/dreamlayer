# teleprompter
# ---------------------------------------------------------------------------

register(BehaviorTemplate(
    name="teleprompter",
    lua_template='''\
-- teleprompter.lua  (generated by Reality Compiler)
local SCROLL_SPEED = ${scroll_speed}
local TILT_CONTROL = ${tilt_control}
local lines        = {"Your text here"}  -- host sends lines via BLE
local offset       = 0
local LINE_H       = 18

local function render()
    frame.display.clear()
    for i, line in ipairs(lines) do
        local y = (i - 1) * LINE_H - offset
        if y > -LINE_H and y < 256 then
            frame.display.text(line, 10, y)
        end
    end
    frame.display.show()
end

while true do
    local speed = SCROLL_SPEED
    if TILT_CONTROL then
        local imu = frame.imu_data()
        if imu and imu.pitch then
            speed = SCROLL_SPEED * (1 + imu.pitch * 2)
        end
    end
    offset = offset + speed
    render()
    frame.sleep(0.1)
end
''',
    host_template=_HOST_TEMPLATE,
    test_spec={"inject": None, "wait_ms": 300, "assert_bright": True},
))

