"""reality_compiler/template_library.py — BehaviorTemplate registry.

Each template has:
  name         — matches BehaviorIntent.type
  lua_template — string.Template Lua code (${variable} substitution)
  host_template— string.Template Python host-glue code
  test_spec    — dict describing emulator injection + assertions
"""
from __future__ import annotations

from dataclasses import dataclass, field
from string import Template
from typing import Any


@dataclass
class BehaviorTemplate:
    name: str
    lua_template: str
    host_template: str
    test_spec: dict[str, Any] = field(default_factory=dict)


# ---------------------------------------------------------------------------
# Template registry
# ---------------------------------------------------------------------------

_REGISTRY: dict[str, BehaviorTemplate] = {}


def register(t: BehaviorTemplate) -> BehaviorTemplate:
    _REGISTRY[t.name] = t
    return t


def get(name: str) -> BehaviorTemplate:
    if name not in _REGISTRY:
        raise KeyError(f"No template registered for behavior: {name!r}")
    return _REGISTRY[name]


def list_behaviors() -> list[str]:
    return sorted(_REGISTRY.keys())


